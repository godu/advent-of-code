module AdventOfCode.Year2021.Day03Spec
  ( spec,
  )
where

import AdventOfCode.Year2021.Day03
  ( process1,
    process2,
    run1,
    run2,
  )
import Test.Hspec
  ( Spec,
    it,
    shouldBe,
  )

spec :: Spec
spec = do
  it "run1" $ do
    run1
      ( unlines
          [ "00100",
            "11110",
            "10110",
            "10111",
            "10101",
            "01111",
            "00111",
            "11100",
            "10000",
            "11001",
            "00010",
            "01010"
          ]
      )
      `shouldBe` "198"

  it "run2" $ do
    run2
      ( unlines
          [ "00100",
            "11110",
            "10110",
            "10111",
            "10101",
            "01111",
            "00111",
            "11100",
            "10000",
            "11001",
            "00010",
            "01010"
          ]
      )
      `shouldBe` "230"
