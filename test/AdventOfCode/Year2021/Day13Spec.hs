module AdventOfCode.Year2021.Day13Spec
  ( spec,
  )
where

import AdventOfCode.Year2021.Day13
  ( run1,
    run2,
  )
import Data.Either (Either)
import Test.Hspec
  ( Spec,
    it,
    shouldBe,
  )

spec :: Spec
spec = do
  it "run1" $ do
    run1
      ( unlines
          [ "6,10",
            "0,14",
            "9,10",
            "0,3",
            "10,4",
            "4,11",
            "6,0",
            "6,12",
            "4,1",
            "0,13",
            "10,12",
            "3,4",
            "3,0",
            "8,4",
            "1,10",
            "2,14",
            "8,10",
            "9,0",
            "",
            "fold along y=7",
            "fold along x=5"
          ]
      )
      `shouldBe` "17"

  it "run2" $ do
    run2
      ( unlines
          [ "6,10",
            "0,14",
            "9,10",
            "0,3",
            "10,4",
            "4,11",
            "6,0",
            "6,12",
            "4,1",
            "0,13",
            "10,12",
            "3,4",
            "3,0",
            "8,4",
            "1,10",
            "2,14",
            "8,10",
            "9,0",
            "",
            "fold along y=7",
            "fold along x=5"
          ]
      )
      `shouldBe` unlines
        [ "#####",
          "#...#",
          "#...#",
          "#...#",
          "#####"
        ]
