spec :: Spec
spec = do
  it "edges" $
    do
      edges
        ( Puzzle $
            M.fromList
              [ ((0, 0), Tile 0 []),
                ((1, 0), Tile 0 [])
              ]
        )
        `shouldBe` S.fromList
          [ (-1, 0),
            (0, -1),
            (0, 1),
            (1, -1),
            (1, 1),
            (2, 0)
          ]

  it "findPlace" $ do
    let (x : y : xs) = read input
    findPlace
      (Puzzle $ M.fromList [((0, 0), vFlip x), ((-1, 0), vFlip y)])
      xs
      `shouldBe` return (-1, 1)

  it "run1" $ do
    run1 input
      `shouldBe` "20899048083289"
